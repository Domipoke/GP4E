/**
 * Switch 
 *  this
 * from 
 *  a to b
 * or from
 *  b to a
 */
function s(w,a,b) {
    if (w==a) {
        return b
    } else {
        return a
    }
}
/**
 * Switch 
 *  this
 * from 
 *  a to b
 * or from
 *  b to a
 */
function cs(w,a,b,ae,be) {
    if (w==a) {
        return be
    } else {
        return ae
    }
}